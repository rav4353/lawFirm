echo -e "${CYAN}â•‘         Veritas AI â€” Deployment Complete! ğŸš€            â•‘${NC}"
echo -e "${CYAN}â• â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•£${NC}"
echo -e "${CYAN}â•‘${NC}  Frontend:     ${GREEN}http://localhost${NC}                       ${CYAN}â•‘${NC}"
echo -e "${CYAN}â•‘${NC}  Backend API:  ${GREEN}http://localhost/api${NC}                   ${CYAN}â•‘${NC}"
echo -e "${CYAN}â•‘${NC}  Backend Docs: ${GREEN}http://localhost/api/docs${NC}              ${CYAN}â•‘${NC}"
echo -e "${CYAN}â•‘${NC}  Grafana:      ${GREEN}http://localhost/grafana${NC}  (admin/veritas) ${CYAN}â•‘${NC}"
