echo -e "${CYAN}â•‘${NC}  Teardown:     ${YELLOW}./deploy.sh teardown${NC}                  ${CYAN}â•‘${NC}"
echo -e "${CYAN}â•‘${NC}  Pod status:   ${YELLOW}kubectl get pods -n veritas${NC}           ${CYAN}â•‘${NC}"
